/**
 * =============================================================================
 * ملف الأدوات المساعدة العامة (utils)
 * -----------------------------------------------------------------------------
 * يحتوي على الدوال المساعدة المشتركة المستخدمة في كامل التطبيق.
 * الدالة الأساسية هنا هي cn المسؤولة عن دمج أصناف (classes) Tailwind CSS.
 * =============================================================================
 */
import { clsx, type ClassValue } from "clsx"
import { twMerge } from "tailwind-merge"

/**
 * تدمج أصناف CSS بطريقة ذكية مع حل التعارضات.
 * أولاً يقوم clsx بدمج الأصناف الشرطية (المصفوفات/الكائنات) في نص واحد،
 * ثم يقوم twMerge بإزالة التعارض بين أصناف Tailwind المتضاربة
 * (مثلاً عند وجود "p-2" و"p-4" يبقي الأخير فقط).
 */
export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs))
}
